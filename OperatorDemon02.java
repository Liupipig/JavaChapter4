package com.itheima.opterator;

public class OperatorDemon02 {
    public static void main(String[] args) {
        //掌握自增、自减运算符
        int a = 10;
        ++a;
        System.out.println(a);

        a--;
        System.out.println(a);

        int i = 10;
        int rs = i++;
        System.out.println(rs);
        System.out.println(i);


    }
}
